export default function Map({ communityId, communityName, posts, isLoading }) {
  const navigation = useNavigation();
  const { colors, isDark } = useTheme();

  if (isLoading) {
    return (
      <View>
        <Text>Loading..</Text>
      </View>
    );
  } else {
    return (
      <View>
        <MapView
          provider={PROVIDER_GOOGLE}
          customMapStyle={isDark ? MapStyle : LightMapStyle2}
          zoomEnabled={true}
          style={styles.map}
          initialRegion={{
            latitude: 40.735255,
            longitude: -74.032775,
            latitudeDelta: 0.00522,
            longitudeDelta: 0.02821,
          }}
        >
          {posts &&
            posts.map((marker, index) => {
              return (
                <Marker
                  key={marker.id}
                  coordinate={{
                    latitude: marker.location.latitude,
                    longitude: marker.location.longitude,
                  }}
                  image={require("../../../assets/marker.png")}
                  tappable={true}
                  onPress={() =>
                    navigation.navigate("PostDetails", {
                      postData: marker,
                      communityId: communityId,
                      postId: marker.id,
                      name: communityName,
                    })
                  }
                >
                  <View
                    style={[
                      styles.marker,
                      { backgroundColor: colors.mapMarkerBgColor },
                    ]}
                  >
                    <Text
                      ellipsizeMode="tail"
                      numberOfLines={1}
                      style={{
                        color: "white",
                        opacity: 1,
                        transform: [{ translateX: 12 }],
                        marginLeft: 30,
                        marginRight: 25,
                      }}
                    >
                      {marker.title}
                    </Text>
                  </View>
                </Marker>
              );
            })}
        </MapView>
      </View>
    );
  }
}

const styles = StyleSheet.create({
  container: {
    flex: 1,
  },
  map: {
    width: "100%",
    height: Dimensions.get("screen").height * 1,
  },
  marker: {
    maxWidth: 200,
    color: "white",
    height: 40,
    backgroundColor: "#00478d",
    borderRadius: 20,
    color: "white",
    alignItems: "center",
    paddingVertical: 12,
    zIndex: -1,
    textOverflow: "ellipsis",
  },
});
